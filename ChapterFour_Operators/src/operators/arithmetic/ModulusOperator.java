package operators.arithmetic;

public class ModulusOperator {

	public static void main(String[] args) {

		int x = 15;
		int y = 4;

		/**
		 * % will provide only reminder
		 */
		System.out.println(x % y); // 3
		System.out.println(x / y); // 3

		System.out.println(5 % 5); // 0
		System.out.println(5 / 5); // 1
	}
}
